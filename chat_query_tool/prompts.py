def language_hint(country: str | None) -> str:
    """
    根据国家代码生成 user prompt 的语言提示前缀。
    country 为空 → 返回空字符串，不附加提示。
    """
    if not country:
        return ""
    code = country.strip().upper()
    lang = COUNTRY_LANGUAGE.get(code, "未在常见列表内（请按多语言常识自行推断）")
    return (
        f"[语言提示] 以下消息发送者位于国家 {code}，主要语言通常是 {lang}。\n"
        f"请优先按该语言理解，再翻译为简体中文。如果实际文本明显不是该语言，仍按实际语言处理。\n\n"
    )


